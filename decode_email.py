def decode_ppurl(ppurl):
    ppurl = str(ppurl)
    query = urlparse.urlparse(ppurl).query
    param = urlparse.parse_qs(query)

    if 'u' not in param:
        sys.stderr.write('ERROR: check if URL is a proofpoint URL')
        return ppurl
    else:
        u = (param['u'][0].replace('-', '%').replace('_', '/'))
        url = urllib.unquote(u)
        return url


def process_payload(e):
    if e.is_multipart():
        for p in e.get_payload():
            process_payload(p)
    else:
        t = e.get_content_type()
        # are there any more formats we should consider?
        if t in ["text/plain", "text/html"]:
            payload = e.get_payload(decode=True)

            # only clean proofpoint-encoded URLs--the "urldefense"
            # prefix might be different across installations
            payload_clean = \
              re.sub(URL_REGEX, \
                     lambda match: decode_ppurl(match.group()) \
                       if "urldefense.proofpoint.com" in match.group() \
                       else match.group(), \
                     payload)

            # modify the payload in place
            e.set_payload(payload_clean)


if __name__ == '__main__':
    # read email from STDIN
    e = email.message_from_string("".join(sys.stdin.readlines()))
    # process and replace URLs in place
    process_payload(e)
    # write email to STDOUT
    sys.stdout.write(str(e))
