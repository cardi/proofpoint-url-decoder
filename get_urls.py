def decode_ppurl(ppurl):
  query = urllib.parse.urlparse(ppurl).query
  param = urllib.parse.parse_qs(query)

  if 'u' not in param:
    sys.stderr.write('ERROR: check if URL is a proofpoint URL')
    return ppurl
  else:
    u   = (param['u'][0].replace('-', '%')
                        .replace('_', '/'))
    url = urllib.parse.unquote(u)
    return url

def process_payload(e):
    if e.is_multipart():
        for p in e.get_payload():
            process_payload(p)
    else:
        t = e.get_content_type()
        if t in ["text/plain", "text/html"]:
            print("type: %s" % t)
            urls = re.findall(URL_REGEX, str(e.get_payload(decode=True)))
            for u in urls:
                if "urldefense.proofpoint.com" in u:
                    u = decode_ppurl(u)
                print(u)
            print("")

if __name__ == '__main__':
    e = email.message_from_string("".join(sys.stdin.readlines()))
    process_payload(e)
